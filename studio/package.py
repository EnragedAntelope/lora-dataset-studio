"""Export: assemble the final ready-to-train dataset folder.

Standalone like every other stage — `package_folder()` exports any folder
that already has NN.txt caption sidecars (however they were produced).
"""

from __future__ import annotations

import json
import re
import shutil
import zipfile
from dataclasses import dataclass
from datetime import datetime
from pathlib import Path
from typing import Iterable


def slugify(name: str) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", name.lower()).strip("-")
    return slug or "character"


@dataclass
class ExportResolution:
    """Classification of candidate export images by caption sidecar state."""
    items: list[tuple[Path, str]]  # (image, non-empty caption) -> exported
    empties: list[str]             # sidecar present but blank (image name)
    missing: list[str]             # no .txt sidecar (image name)


def resolve_export_items(image_paths: Iterable[Path]) -> ExportResolution:
    """Read each image's sibling .txt and classify it for export.

    Order-preserving. Full paths in, so identically-named files in different
    folders never collide. Only `items` are packaged; `empties`/`missing`
    surface as pre-export warnings.
    """
    items: list[tuple[Path, str]] = []
    empties: list[str] = []
    missing: list[str] = []
    for img in image_paths:
        txt = img.with_suffix(".txt")
        if not txt.exists():
            missing.append(img.name)
            continue
        caption = txt.read_text(encoding="utf-8").strip()
        if caption:
            items.append((img, caption))
        else:
            empties.append(img.name)
    return ExportResolution(items=items, empties=empties, missing=missing)


def package_dataset(
    items: list[tuple[Path, str]],  # (image_path, final_caption)
    output_root: Path,
    character_name: str,
    trigger: str,
    metadata: dict,
) -> Path:
    ds_name = f"{slugify(character_name or trigger or 'character')}-dataset"
    ds_dir = output_root / ds_name
    n = 1
    while ds_dir.exists():  # never clobber an existing dataset
        n += 1
        ds_dir = output_root / f"{ds_name}-{n}"
    ds_dir.mkdir(parents=True)

    jsonl_lines: list[str] = []
    for i, (img, caption) in enumerate(items, start=1):
        stem = f"{i:02d}"
        shutil.copy2(img, ds_dir / f"{stem}.png")
        (ds_dir / f"{stem}.txt").write_text(caption, encoding="utf-8")
        # HuggingFace `imagefolder` caption metadata, so the dataset also loads
        # directly via datasets.load_dataset("imagefolder", ...) with captions.
        jsonl_lines.append(json.dumps({"file_name": f"{stem}.png", "text": caption}))
    (ds_dir / "metadata.jsonl").write_text("\n".join(jsonl_lines) + "\n", encoding="utf-8")

    metadata = {"created": datetime.now().isoformat(timespec="seconds"), **metadata}
    (ds_dir / "metadata.json").write_text(json.dumps(metadata, indent=2, default=str), encoding="utf-8")

    readme = [
        f"LoRA training dataset for: {character_name or '(unnamed character)'}",
        f"Trigger word: {trigger or '(none)'}",
        f"Images: {len(items)} (image NN.png pairs with caption NN.txt)",
        "",
        "Generated by LoRA Dataset Studio.",
        "Folder format is directly usable by ai-toolkit and OneTrainer",
        "(point the trainer at this folder; captions are the .txt files).",
        "metadata.jsonl mirrors the captions in HuggingFace 'imagefolder' form.",
    ]
    (ds_dir / "README.txt").write_text("\n".join(readme), encoding="utf-8")
    return ds_dir


def zip_dataset(ds_dir: Path) -> Path:
    """Zip a packaged dataset folder into a sibling `<name>.zip` (non-clobbering).

    Entries are stored under the dataset folder's own name, so it extracts into a
    tidy folder rather than a pile of loose files. Only files inside `ds_dir` are
    added, and arcnames are derived from `ds_dir` (never absolute), so the archive
    has no path-traversal surface. Convenient for uploading to a cloud trainer.
    """
    ds_dir = Path(ds_dir)
    if not ds_dir.is_dir():
        raise RuntimeError(f"Not a dataset folder: {ds_dir}")
    zip_path = ds_dir.parent / f"{ds_dir.name}.zip"
    n = 1
    while zip_path.exists():  # never clobber an existing archive
        n += 1
        zip_path = ds_dir.parent / f"{ds_dir.name}-{n}.zip"
    files = sorted(p for p in ds_dir.rglob("*") if p.is_file())
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for f in files:
            zf.write(f, arcname=f"{ds_dir.name}/{f.relative_to(ds_dir).as_posix()}")
    return zip_path


def package_folder(
    folder: Path,
    output_root: Path,
    character_name: str = "",
    trigger: str = "",
    only: list[Path] | None = None,
) -> Path:
    """Export a folder whose images already have .txt caption sidecars."""
    from studio.config import list_images

    images = only if only else list_images(folder)
    items: list[tuple[Path, str]] = []
    missing: list[str] = []
    for img in images:
        txt = img.with_suffix(".txt")
        if txt.exists():
            items.append((img, txt.read_text(encoding="utf-8").strip()))
        else:
            missing.append(img.name)
    if not items:
        raise RuntimeError(
            f"No captioned images in {folder} — run captioning first (each image "
            f"needs a matching .txt sidecar)."
        )
    metadata = {
        "character_name": character_name,
        "trigger": trigger,
        "source_folder": str(folder),
        "skipped_uncaptioned": missing,
    }
    return package_dataset(items, output_root, character_name, trigger, metadata)
